async function reseedWardrobe() {
  const client = new MongoClient(MONGODB_URI);
  
  try {
    await client.connect();
    console.log("Connected to MongoDB");
    
    const db = client.db(DB_NAME);
    const collection = db.collection("communityWardrobe");
    
    // Drop existing collection to re-seed with images
    console.log("Dropping existing wardrobe collection...");
    await collection.drop().catch(() => console.log("Collection didn't exist, creating new..."));
    
    // Create indexes
    await collection.createIndex({ type: 1, isPublic: 1 });
    await collection.createIndex({ tags: 1 });
    await collection.createIndex({ characterEntityId: 1 });
    await collection.createIndex({ rarity: 1 });
    await collection.createIndex({ usageCount: -1 });
    await collection.createIndex({ 
      name: "text", 
      description: "text", 
      tags: "text",
      promptText: "text",
    });
    
    console.log("Indexes created");
    
    // Insert all wardrobe items with images
    const now = new Date();
    const itemsToInsert = WARDROBE_ITEMS.map(item => ({
      _id: new ObjectId(),
      ...item,
      usageCount: 0,
      favoriteCount: 0,
      createdAt: now,
      createdBy: SYSTEM_USER_ID,
      updatedAt: now,
      updatedBy: SYSTEM_USER_ID,
    }));
    
    const result = await collection.insertMany(itemsToInsert);
    console.log(`\nâœ… Inserted ${result.insertedCount} wardrobe items with images!`);
    
    // Summary
    const withImages = WARDROBE_ITEMS.filter(i => i.imageUrl).length;
    console.log(`\nðŸ“¸ Items with images: ${withImages}/${WARDROBE_ITEMS.length}`);
    
  } catch (err) {
    console.error("Error seeding wardrobe:", err);
  } finally {
    await client.close();
    console.log("\nDisconnected from MongoDB");
  }
}

reseedWardrobe();
